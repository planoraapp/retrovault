// Fun√ß√£o para obter a capa de um jogo
export const getGameCover = (gameName: string): string => {
  // Normalizar o nome do jogo para chave
  const normalizedName = gameName
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, '') // Remove caracteres especiais
    .replace(/\s+/g, '-') // Substitui espa√ßos por h√≠fens
    .trim()
  
  // Debug tempor√°rio para Chrono Trigger
  if (gameName.toLowerCase().includes('chrono')) {
    console.log(`üîç Chrono Debug - Original: "${gameName}", Normalized: "${normalizedName}"`)
    console.log(`üéØ Available keys:`, Object.keys(gameCovers).filter(k => k.includes('chrono')))
  }
  
  return gameCovers[normalizedName] || ''
}

// Fun√ß√£o para verificar se existe capa para o jogo
export const hasGameCover = (gameName: string): boolean => {
  return getGameCover(gameName) !== ''
}

